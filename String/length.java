package String;

public class length
{
    public static void main(String[] args)
    {
        String string_1 = "www.emhlz.rocks";
        String string_2 = "emhlz";

        System.out.println("string_1的长度为：");
        System.out.println(string_1.length());

        System.out.println("string_2的长度为：");
        System.out.println(string_2.length());
    }
}

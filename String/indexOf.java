package String;

public class indexOf
{
    public static void main(String[] args)
    {
        String string = "hcjlovezdr";

        System.out.println(string.indexOf('z'));

        System.out.println(string.indexOf(100));
        System.out.println(string.indexOf('d'));

        System.out.println(string.indexOf('r',3));

    }
}
